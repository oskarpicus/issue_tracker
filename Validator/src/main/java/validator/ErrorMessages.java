package validator;

/**
 * Class that encapsulates all the error messages that a validation error can raise
 */
public class ErrorMessages {

    /**
     * Error message for when the username of an {@code User} is invalid
     */
    public static final String USER_INVALID_USERNAME = "Invalid username\n";

    /**
     * Error message for when the password of an {@code User} is invalid
     */
    public static final String USER_INVALID_PASSWORD = "Invalid password\n";

    /**
     * Error message for when the first name of an {@code User} is invalid
     */
    public static final String USER_INVALID_FIRST_NAME = "Invalid first name\n";

    /**
     * Error message for when the last name of an {@code User} is invalid
     */
    public static final String USER_INVALID_LAST_NAME = "Invalid last name\n";

    /**
     * Error message for when the email of an {@code User} is invalid
     */
    public static final String USER_INVALID_EMAIL = "Invalid email\n";
}
