package validator;

/**
 * Class that encapsulates all the error messages that a validation error can raise
 */
public class ErrorMessages {

    /**
     * Error message for when the username of n {@code User} is invalid
     */
    public static final String USER_INVALID_USERNAME = "Invalid username\n";

    /**
     * Error message for when the password of n {@code User} is invalid
     */
    public static final String USER_INVALID_PASSWORD = "Invalid password\n";

    /**
     * Error message for when the first name of n {@code User} is invalid
     */
    public static final String USER_INVALID_FIRST_NAME = "Invalid first name\n";

    /**
     * Error message for when the last name of n {@code User} is invalid
     */
    public static final String USER_INVALID_LAST_NAME = "Invalid last name\n";

    /**
     * Error message for when the email of n {@code User} is invalid
     */
    public static final String USER_INVALID_EMAIL = "Invalid email\n";

    /**
     * Error message for when the title of a {@code Project} is invalid
     */
    public static final String PROJECT_INVALID_TITLE = "Invalid title\n";

    /**
     * Error message for when the description of a {@code Project} is invalid
     */
    public static final String PROJECT_INVALID_DESCRIPTION = "Invalid description\n";
}
