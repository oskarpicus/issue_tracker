    public static final String INVOLVEMENT_INVALID_ROLE = "Invalid role\n";

    public static final String ISSUE_INVALID_TITLE = "Invalid title\n";

    public static final String ISSUE_INVALID_SEVERITY = "Invalid severity\n";

